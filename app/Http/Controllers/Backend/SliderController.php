     */
    public function store (Request $request)
    {
        
        
        //print_r($request->all());
        $data = new Slider;

        $data->title=$request->title;

        $data->order=$request->order;
        $data->image=$request->image->store('public/gallery/slider');
       
        $data->save();

        return back()->with('status','Successfully Uploaded!');


        
    }









